package xml;

public class StudentBeam {
  private int id;
  private int group;
  private String name;
  private String sex;
  private int age;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getGroup() {
	return group;
}
public void setGroup(int group) {
	this.group = group;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getSex() {
	return sex;
}
public void setSex(String sex) {
	this.sex = sex;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
  
  
}
